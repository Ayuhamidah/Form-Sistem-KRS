    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String Kode_MK = text_kode_mk.getText();
        String Mata_Kuliah = text_mata_kuliah.getText();
        String SKS = text_sks.getText();
        String SMT = text_smt.getText();
        String Kelas = text_kelas.getText();
        String Jadwal = text_jadwal.getText();
        String Status = text_status.getText();
        
        String SQL = "insert into entri_krs (kode_mk, mata_kuliah, sks, "
                +"smt, kelas, jadwal,status) values ('"+Kode_MK+"','"+Mata_Kuliah+"',"
                + "'"+SKS+"','"+SMT+"','"+Kelas+"','"+Jadwal+"','"+Status+"')";

        System.out.print(SQL);
        try
            {
                KoneksiDB.statement.executeUpdate(SQL);
                
                JOptionPane.showMessageDialog(rootPane, "Data Berhasil ditambahkan");
               
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, "Data gagal ditambahkan");
               
            }
    }//GEN-LAST:event_jButton2ActionPerformed

